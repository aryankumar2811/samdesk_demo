export function searchSignals(query: string, limit = 10): Signal[] {
  const q = query.toLowerCase();
  return SIGNALS.filter(
    (s) =>
      s.text.toLowerCase().includes(q) ||
      (s.place && s.place.toLowerCase().includes(q))
  ).slice(0, limit);
}
